package com.dao;

public class Herb {
	String herbID;
	double weight;
	public String getHerbID() {
		return herbID;
	}
	public void setHerbID(String herbID) {
		this.herbID = herbID;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	
}
